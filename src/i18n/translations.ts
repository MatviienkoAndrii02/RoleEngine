export function isLanguage(value: string | undefined | null): value is Language {
  return languages.includes(value as Language);
}

export function translate(language: Language, key: TranslationKey, params?: Record<string, string | number>): string {
  const template: string = translations[language][key] ?? translations[defaultLanguage][key] ?? key;
  if (!params) return template;
  return Object.entries(params).reduce((text, [name, value]) => text.replaceAll(`{${name}}`, String(value)), template);
}
